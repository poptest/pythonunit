class Calcultor():

    @classmethod
    def jia(cls, num1, num2):
        return (num1 + num2)

    @classmethod
    def jian(cls, num1, num2):
        return (num1 - num2)

    @classmethod
    def cheng(cls, num1, num2):
        return (num1 * num2)

    @classmethod
    def chu(cls, num1, num2):
        return (num1 / num2)